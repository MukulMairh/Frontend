//Boolean Logic


// AND(&&), OR (||) NOT(!), change values of the below ones. 
const a = true;
const b = false;


//AND Example 
if (a && b)
    console.log("True");
else
    console.log("False");

//OR Example 
if (a || b)
    console.log("True");
else
    console.log("False");

//NOT Example 
if (!a)
    console.log("True");
else
    console.log("False");